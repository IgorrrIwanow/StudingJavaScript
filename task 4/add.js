/**
 * Функция складывает параметры.
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function sum(a, b) {
    return a + b;
}
/**
 * Функция вычитает из параметра "a" параметр "b".
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function sub(a, b) {
    return a - b;
}
/**
 * Функция перемножает параметры.
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function mul(a, b) {
    return a * b;
}
/**
 * Функция делит параметр "a" на параметр "b".
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */
function div(a, b) {
    return a / b;
}